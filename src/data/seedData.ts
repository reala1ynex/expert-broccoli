function makePlanting(
  idValue: string,
  farmId: string,
  name: string,
  cropId: string,
  environmentId: string,
  bedOrUnitId: string,
  growingMethodIds: string[],
  mediumIds: string[],
  seedDate: string,
  plantCount: number,
  areaSqFt: number
): Planting {
  const cropData = cropCatalog.find((item) => item.id === cropId)!;
  const dates = planDates(cropData, seedDate, cropData.transplantTimingDays > 0 ? "indoor_start" : cropData.cropType === "microgreen" ? "microgreen_sowing" : "direct_seed");
  const expectedYield = cropData.estimatedYieldBasis === "per_plant" ? cropData.estimatedYield * plantCount : cropData.estimatedYield * areaSqFt;
  const expectedRevenue = expectedYield * cropData.estimatedPricePerUnit;
  return {
    id: idValue,
    farmId,
    cropId,
    name,
    startMethod: cropData.cropType === "microgreen" ? "microgreen_sowing" : cropData.transplantTimingDays > 0 ? "indoor_start" : "direct_seed",
    environmentId,
    bedOrUnitId,
    growingMethodIds,
    mediumIds,
    seedDate: dates.seedDate,
    transplantDate: dates.transplantDate,
    firstHarvestDate: dates.firstHarvestDate,
    harvestWindowDays: dates.harvestWindowDays,
    terminationDate: dates.terminationDate,
    successionIndex: 1,
    plantCount,
    areaSqFt,
    spacingIn: cropData.spacingIn,
    expectedYield,
    expectedRevenue,
    laborHoursEstimate: Math.max(1, Math.round((plantCount / 50) * 10) / 10),
    irrigationProfile: { mode: growingMethodIds.includes("method_nft") ? "recirculating" : "drip", frequency: "daily check", fertigation: growingMethodIds.some((m) => ["method_nft", "method_dutch_bucket", "method_dwc"].includes(m)), targetPh: cropData.preferredPhRange, targetEc: cropData.preferredEcRange },
    status: new Date(`${dates.firstHarvestDate}T00:00:00`) <= new Date() ? "harvesting" : "active",
    notes: "",
    createdAt: now(),
    updatedAt: now()
  };
}

